
//for loop
//Summation upto 100

var sum = 0;
for (let i=0; i<=100; i++)
{ 
	
	sum = sum+i;
}
console.log("The sum using for loop is: " + sum)

//While loop
//Summation upto 100
var i=0
var sum = 0;
while(i<=100)
{
	sum = sum+i;
	i++;
}
console.log("The sum using while is: " + sum)

//do While loop
//Summation upto 100
var i=0;
var sum = 0;
do
{
	sum = sum+i;
	i++;
}while(i<=100);
console.log("The sum using do while is: " + sum)

